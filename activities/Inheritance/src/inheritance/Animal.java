package inheritance;

public class Animal {
    private String name;
    private int age,legs;

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return age;
    }

    public void setLegs(int legs) {
        this.legs = legs;
    }
    public int getLegs() {
        return legs;
    }
}
